
print('Vou realizar a analise de um número!')
n = int(input('Digite um número: '))
ant = n - 1
suc = n + 1
print('Analisando o número {} digitado, o antecessor é {} e o sucessor é {}.'.format(n, ant, suc))
print('Obrigado!')
